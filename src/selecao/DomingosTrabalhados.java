package selecao;

public class DomingosTrabalhados {
	
	private Funcionario funcionario;
	private int totalDomingos; // total de domingos trabalhados no mes
	
	public Funcionario getFuncionario() {
		return funcionario;
	}
	public void setFuncionario(Funcionario funcionario) {
		this.funcionario = funcionario;
	}
	public int getTotalDomingos() {
		return totalDomingos;
	}
	public void setTotalDomingos(int totalDomingos) {
		this.totalDomingos = totalDomingos;
	}
	
	
	

}
